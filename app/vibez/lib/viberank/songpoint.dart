class SongPoint {


  final String songName;
  final String artist;

  // positions in 12 dimensions
  final int x;
  final int y;
  final int z;
  final int a;
  final int b;
  final int c;
  final int d;
  final int e;
  final int trippy;
  final int chill;
  final int hype;
  final int party;

  //the weight of the song point
  final int weight;

  SongPoint({this.songName, this.artist, this.x, this.y, this.z, this.a, this.b, this.c, this.d, this.e, this.trippy, this.chill, this.hype, this.party, this.weight});


}

